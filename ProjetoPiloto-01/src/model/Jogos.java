package model;

import java.util.Date;

public class Jogos {
	
	private int codigoJogo;
	private int codTimeA;
	private int golsTimeA;
	private int codTimeB;
	private int golsTimeB;
	private Date dataJogos;
	
	
	
	public int getCodigoJogo() {
		return codigoJogo;
	}
	public void setCodigoJogo(int codigoJogo) {
		this.codigoJogo = codigoJogo;
	}
	public int getCodTimeA() {
		return codTimeA;
	}
	public void setCodTimeA(int codTimeA) {
		this.codTimeA = codTimeA;
	}
	public int getGolsTimeA() {
		return golsTimeA;
	}
	public void setGolsTimeA(int golsTimeA) {
		this.golsTimeA = golsTimeA;
	}
	public int getCodTimeB() {
		return codTimeB;
	}
	public void setCodTimeB(int codTimeB) {
		this.codTimeB = codTimeB;
	}
	public int getGolsTimeB() {
		return golsTimeB;
	}
	public void setGolsTimeB(int golsTimeB) {
		this.golsTimeB = golsTimeB;
	}
	public Date getDataJogos() {
		return dataJogos;
	}
	public void setDataJogos(Date dataJogos) {
		this.dataJogos = dataJogos;
	}
	
	
	

}
